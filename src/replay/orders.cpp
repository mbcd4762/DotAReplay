static const int numOrderStrings = sizeof orderStrings / sizeof orderStrings[0];
char const* orderId2String(uint32 id)
{
  for (int i = 0; i < numOrderStrings; i++)
    if (orderStrings[i].id == id)
      return orderStrings[i].order;
  return NULL;
}
String id2String(uint32 id)
{
  String str = "";
  for (int i = 24; i >= 0; i -= 8)
    if ((id >> i) & 0xFF)
      str += char((id >> i) & 0xFF);
  return str;
}

bool isGoodId(uint32 id)
{
  for (int i = 0; i < 4; i++)
  {
    int m = int((unsigned char) (id & 0xFF));
    if ((m < '0' || m > '9') &&
        (m < 'A' || m > 'Z') &&
        (m < 'a' || m > 'z'))
      return false;
    id >>= 8;
  }
  return true;
}
